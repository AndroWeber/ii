def generate_test_plan(description):
    # Генерация тест-плана на основе описания задачи
    test_plan = f"""
Тест-план: {description}

1. Цель:
   - Проверить корректность выполнения задачи: {description}.

2. Шаги:
   1. Открыть тестируемую страницу.
   2. Проверить отображение элементов интерфейса.
   3. Взаимодействовать с элементами, например:
      - Ввод данных в поля.
      - Нажатие кнопок.
   4. Проверить ожидаемое поведение.

3. Ожидаемые результаты:
   - Все элементы работают корректно.
   - Ошибок валидации нет.
   - Нагрузочные тесты проходят успешно (если применимо).

4. Дополнительные проверки:
   - Тест на производительность.
   - Проверка безопасности (если применимо).

"""
    return test_plan